-module(openweather_proxy).
-author("nsharma").

-behaviour(gen_server).

%% API
-export([start_link/0]).

%% gen_server callbacks
-export([init/1,
  handle_call/3,
  handle_cast/2,
  handle_info/2,
  terminate/2,
  code_change/3]).

-define(SERVER, ?MODULE).

%% Set the openweather.org api key here
%% TODO move out of code and let it be configured at runtime instead.
-define(API_KEY, "<API_KEY_HERE>").
-define(MAXIMUM_CITIES, 20).
-define(THIRD_PARTY_GEO_FLOAT_PRECISION, 1).

-record(state, {}).

%%%===================================================================
%%% API
%%%===================================================================

%%--------------------------------------------------------------------
%% @doc
%% Starts the server
%%
%% @end
%%--------------------------------------------------------------------
-spec(start_link() ->
  {ok, Pid :: pid()} | ignore | {error, Reason :: term()}).
start_link() ->
  gen_server:start_link({local, ?SERVER}, ?MODULE, [], []).

%%%===================================================================
%%% gen_server callbacks
%%%===================================================================

%%--------------------------------------------------------------------
%% @private
%% @doc
%% Initializes the server
%%
%% @spec init(Args) -> {ok, State} |
%%                     {ok, State, Timeout} |
%%                     ignore |
%%                     {stop, Reason}
%% @end
%%--------------------------------------------------------------------
-spec(init(Args :: term()) ->
  {ok, State :: #state{}} | {ok, State :: #state{}, timeout() | hibernate} |
  {stop, Reason :: term()} | ignore).
init([]) ->
  {ok, #state{}}.

%%--------------------------------------------------------------------
%% @private
%% @doc
%% Handling call messages or synchronous requests from clients.
%%
%% @todo keep track of number of extternal API calls per time period,
%%       say a minute. The free quota is capped at 60 calls per min.
%% @end
%%--------------------------------------------------------------------
-spec(handle_call(Request :: term(), From :: {pid(), Tag :: term()},
    State :: #state{}) ->
  {reply, Reply :: term(), NewState :: #state{}} |
  {reply, Reply :: term(), NewState :: #state{}, timeout() | hibernate} |
  {noreply, NewState :: #state{}} |
  {noreply, NewState :: #state{}, timeout() | hibernate} |
  {stop, Reason :: term(), Reply :: term(), NewState :: #state{}} |
  {stop, Reason :: term(), NewState :: #state{}}).
handle_call({geocircle, GeoLat, GeoLong, CityCount}, _From, State) ->
  true = GeoLat > -91 andalso GeoLat < 91,
  true = GeoLong > -181 andalso GeoLong < 181,
  true = CityCount > 0 andalso CityCount =< ?MAXIMUM_CITIES,
  ValidatedLatVal = float_to_list(GeoLat,
    [{decimals, ?THIRD_PARTY_GEO_FLOAT_PRECISION}]),
  ValidatedLongVal = float_to_list(GeoLong,
    [{decimals, ?THIRD_PARTY_GEO_FLOAT_PRECISION}]),
  ValidatedCityCount = integer_to_list(CityCount),
  % The /find api looks for latitude and longitude and find 10 (cnt=10)
  % cities for weather information.
  % see http://openweathermap.org/current for more details.
  Url = "http://api.openweathermap.org/data/2.5/find?lat=" ++
    ValidatedLatVal ++ "&lon=" ++ ValidatedLongVal ++
    "&cnt=" ++ ValidatedCityCount ++
    "&appid=" ++ ?API_KEY,
  lager:debug("fetching Url=~s~n", [Url]),
  WeatherResponse = httpc:request(Url),
  case WeatherResponse of
    {ok, ResponseContents} ->
      {_RespFirstLine, _RespHeaders, ResponseBody} = ResponseContents,
      {reply, {ok, ResponseBody}, State};
    _ ->
      {reply, {error, fetcherror}, State}
  end.
%handle_call(_Request, _From, State) ->
%  {reply, ok, State}.

%%--------------------------------------------------------------------
%% @private
%% @doc
%% Handling cast messages
%%
%% @end
%%--------------------------------------------------------------------
-spec(handle_cast(Request :: term(), State :: #state{}) ->
  {noreply, NewState :: #state{}} |
  {noreply, NewState :: #state{}, timeout() | hibernate} |
  {stop, Reason :: term(), NewState :: #state{}}).
handle_cast(_Request, State) ->
  {noreply, State}.

%%--------------------------------------------------------------------
%% @private
%% @doc
%% Handling all non call/cast messages
%%
%% @spec handle_info(Info, State) -> {noreply, State} |
%%                                   {noreply, State, Timeout} |
%%                                   {stop, Reason, State}
%% @end
%%--------------------------------------------------------------------
-spec(handle_info(Info :: timeout() | term(), State :: #state{}) ->
  {noreply, NewState :: #state{}} |
  {noreply, NewState :: #state{}, timeout() | hibernate} |
  {stop, Reason :: term(), NewState :: #state{}}).
handle_info(_Info, State) ->
  {noreply, State}.

%%--------------------------------------------------------------------
%% @private
%% @doc
%% This function is called by a gen_server when it is about to
%% terminate. It should be the opposite of Module:init/1 and do any
%% necessary cleaning up. When it returns, the gen_server terminates
%% with Reason. The return value is ignored.
%%
%% @spec terminate(Reason, State) -> void()
%% @end
%%--------------------------------------------------------------------
-spec(terminate(Reason :: (normal | shutdown | {shutdown, term()} | term()),
    State :: #state{}) -> term()).
terminate(_Reason, _State) ->
  ok.

%%--------------------------------------------------------------------
%% @private
%% @doc
%% Convert process state when code is changed
%%
%% @spec code_change(OldVsn, State, Extra) -> {ok, NewState}
%% @end
%%--------------------------------------------------------------------
-spec(code_change(OldVsn :: term() | {down, term()}, State :: #state{},
    Extra :: term()) ->
  {ok, NewState :: #state{}} | {error, Reason :: term()}).
code_change(_OldVsn, State, _Extra) ->
  {ok, State}.

